    "class PoissonBrackets:\n",
    "    \"\"\"Estructura simpléctica con corchetes de Poisson {f, g}.\"\"\"\n",
    "\n",
    "    @staticmethod\n",
    "    def _to_scalar(val: Union[np.ndarray, float]) -> float:\n",
    "        \"\"\"Convierte un array de un solo elemento (0-D o 1-D) a un escalar float.\"\"\"\n",
    "        if isinstance(val, np.ndarray):\n",
    "            return val.item()\n",
    "        return float(val)\n",
    "\n",
    "    @staticmethod\n",
    "    def poisson_bracket(f: Callable, g: Callable,\n",
    "                       q: np.ndarray, p: np.ndarray,\n",
    "                       eps: float = 1e-5) -> float:\n",
    "        \"\"\"Calcula {f, g} = (∂f/∂q)(∂g/∂p) - (∂f/∂p)(∂g/∂q)\"\"\"\n",
    "        # Ensure q and p are arrays, even if single-element\n",
    "        q = np.atleast_1d(q)\n",
    "        p = np.atleast_1d(p)\n",
    "\n",
    "        df_dq_val = (f(q + eps, p) - f(q - eps, p)) / (2 * eps)\n",
    "        df_dp_val = (f(q, p + eps) - f(q, p - eps)) / (2 * eps)\n",
    "        dg_dq_val = (g(q + eps, p) - g(q - eps, p)) / (2 * eps)\n",
    "        dg_dp_val = (g(q, p + eps) - g(q, p - eps)) / (2 * eps)\n",
    "\n",
    "        df_dq = PoissonBrackets._to_scalar(df_dq_val)\n",
    "        df_dp = PoissonBrackets._to_scalar(df_dp_val)\n",
    "        dg_dq = PoissonBrackets._to_scalar(dg_dq_val)\n",
    "        dg_dp = PoissonBrackets._to_scalar(dg_dp_val)\n",
    "\n",
    "        return float(df_dq * dg_dp - df_dp * dg_dq)\n",
    "\n",
    "    @staticmethod\n",
    "    def liouville_evolution(H: Callable, f: Callable,\n",
    "                           q: np.ndarray, p: np.ndarray) -> float:\n",
    "        \"\"\"Ecuación de Liouville: df/dt = {f, H}\"\"\"\n",
    "        return PoissonBrackets.poisson_bracket(f, H, q, p)\n",
    "\n",
    "\n",
    "# ============================================================================\n",
    "# ESTRUCTURA METRIPLÉCTICA\n",
    "# ============================================================================\n",
    "\n",
    "class MetriplecticStructure:\n",
    "    \"\"\"Estructura metripléctica: parte simpléctica + parte métrica (disipativa).\"\"\"\n",
    "\n",
    "    @staticmethod\n",
    "    def metriplectic_bracket(f: Callable, g: Callable,\n",
    "                            q: np.ndarray, p: np.ndarray,\n",
    "                            M: np.ndarray,\n",
    "                            eps: float = 1e-5) -> float:\n",
    "        \"\"\"Corchete metriplexico: [f, g] = {f, g} + [f, g]_M\"\"\"\n",
    "        # Ensure q and p are arrays, even if single-element\n",
    "        q = np.atleast_1d(q)\n",
    "        p = np.atleast_1d(p)\n",
    "\n",
    "        poisson_part = PoissonBrackets.poisson_bracket(f, g, q, p, eps)\n",
    "\n",
    "        df_dq_val = (f(q + eps, p) - f(q - eps, p)) / (2 * eps)\n",
    "        df_dp_val = (f(q, p + eps) - f(q, p - eps)) / (2 * eps)\n",
    "        grad_f_q = PoissonBrackets._to_scalar(df_dq_val)\n",
    "        grad_f_p = PoissonBrackets._to_scalar(df_dp_val)\n",
    "        grad_f = np.array([grad_f_q, grad_f_p])\n",
    "\n",
    "        dg_dq_val = (g(q + eps, p) - g(q - eps, p)) / (2 * eps)\n",
    "        dg_dp_val = (g(q, p + eps) - g(q, p - eps)) / (2 * eps)\n",
    "        grad_g_q = PoissonBrackets._to_scalar(dg_dq_val)\n",
    "        grad_g_p = PoissonBrackets._to_scalar(dg_dp_val)\n",
    "        grad_g = np.array([grad_g_q, grad_g_p])\n",
    "\n",
    "        metric_part = np.dot(grad_f, np.dot(M, grad_g))\n",
    "        return float(poisson_part + metric_part)\n",
    "\n",
    "    @staticmethod\n",
    "    def metriplectic_evolution(H: Callable, S: Callable, f: Callable,\n",
    "                              q: np.ndarray, p: np.ndarray,\n",
    "                              M: np.ndarray,\n",
    "                              eps: float = 1e-5) -> float:\n",
    "        \"\"\"Ecuación metripléctica: df/dt = {f, H} + [f, S]_M\"\"\"\n",
    "        hamiltonian_part = PoissonBrackets.poisson_bracket(f, H, q, p, eps)\n",
    "        dissipative_part = MetriplecticStructure.metriplectic_bracket(f, S, q, p, M, eps)\n",
    "        return float(hamiltonian_part + dissipative_part)\n",
    "\n",
