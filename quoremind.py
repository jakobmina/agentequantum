    "\n",
    "\n",
    "def aureo_operator(n: int, phi: float = 1.6180339887) -> Tuple[float, float]:\n",
    "    \"\"\"Calcula paridad y fase del operador áureo Ô_n.\"\"\"\n",
    "    n_float = float(n)\n",
    "    paridad = np.cos(np.pi * n_float)\n",
    "    fase_mod = np.cos(np.pi * phi * n_float)\n",
    "    return paridad, fase_mod\n",
    "\n",
    "\n",
    "def lambda_doble_operator(state_vector: np.ndarray,\n",
    "                         hamiltonian: Callable,\n",
    "                         qubits: np.ndarray,\n",
    "                         golden_phase: int = 0) -> float:\n",
    "    \"\"\"\n",
    "    Parámetro λ_doble: combina amplitud + operador áureo + qubits para detectar anomalías.\n",
    "    λ_doble = (weighted_sum * aureo_component * hamiltonian_effects) / mean_qbits\n",
    "    \"\"\"\n",
    "    amplitude = np.max(np.abs(state_vector))\n",
    "    weighted_sum = np.sum(state_vector * amplitude)\n",
    "    mean_qbits = np.mean(qubits)\n",
    "    mean_qbits = max(mean_qbits, 1e-6)\n",
    "\n",
    "    aureo_op = golden_ratio_operator(len(state_vector), golden_phase)\n",
    "    aureo_component = np.abs(np.dot(aureo_op, state_vector))\n",
    "\n",
    "    hamiltonian_effects = np.mean([hamiltonian(q, 0.0) for q in qubits])\n",
    "\n",
    "    lambda_doble = (weighted_sum * aureo_component * hamiltonian_effects) / mean_qbits\n",
    "    return float(np.real(lambda_doble))\n",
    "\n",
    "\n",
    "def calculate_cosines(entropy: float, prn_object: float) -> Tuple[float, float, float]:\n",
    "    \"\"\"Calcula los cosenos directores (x, y, z) para un vector 3D.\"\"\"\n",
    "    epsilon = 1e-6\n",
    "    entropy = max(entropy, epsilon)\n",
    "    prn_object = max(prn_object, epsilon)\n",
    "    magnitude = np.sqrt(entropy ** 2 + prn_object ** 2 + 1)\n",
    "    cos_x = entropy / magnitude\n",
    "    cos_y = prn_object / magnitude\n",
    "    cos_z = 1 / magnitude\n",
    "    return cos_x, cos_y, cos_z\n",
    "\n",
    "\n",
    "# Definir observables globales\n",
    "H = lambda q, p: 0.5 * p**2 + 0.5 * q**2\n",
    "S = lambda q, p: -0.5 * np.log(q**2 + p**2 + 1e-6)\n",
