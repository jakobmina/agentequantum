    "class VonNeumannEntropy:\n",
    "    \"\"\"Entropía de von Neumann para operadores de densidad cuánticos.\"\"\"\n",
    "\n",
    "    @staticmethod\n",
    "    def compute_von_neumann_entropy(density_matrix: np.ndarray,\n",
    "                                   epsilon: float = 1e-15,\n",
    "                                   normalize: bool = True,\n",
    "                                   method: str = \"sigmoid\") -> float:\n",
    "        \"\"\"\n",
    "        Calcula S = -Tr(ρ log ρ) donde ρ es la matriz de densidad.\n",
    "        \"\"\"\n",
    "        eigenvalues = np.linalg.eigvalsh(density_matrix)\n",
    "        eigenvalues = np.clip(eigenvalues, epsilon, None)\n",
    "        entropy = -np.sum(eigenvalues * np.log2(eigenvalues))\n",
    "\n",
    "        if not normalize:\n",
    "            return float(entropy)\n",
    "\n",
    "        dim = len(eigenvalues)\n",
    "        max_entropy = np.log2(dim)\n",
    "\n",
    "        if method == \"sigmoid\":\n",
    "            normalized = 1.0 / (1.0 + np.exp(-entropy))\n",
    "        elif method == \"tanh\":\n",
    "            normalized = (np.tanh(entropy / 2.0) + 1.0) / 2.0\n",
    "        elif method == \"log_compression\":\n",
    "            normalized = np.log(1.0 + entropy) / np.log(1.0 + max_entropy)\n",
    "        elif method == \"min_max\":\n",
    "            normalized = entropy / max_entropy\n",
    "        elif method == \"clamp\":\n",
    "            normalized = np.clip(entropy / max_entropy, 0.0, 1.0)\n",
    "        else:\n",
    "            raise ValueError(f\"Método desconocido: {method}\")\n",
    "\n",
    "        return float(np.clip(normalized, 0.0, 1.0))\n",
    "\n",
    "    @staticmethod\n",
    "    def density_matrix_from_state(state: np.ndarray) -> np.ndarray:\n",
    "        \"\"\"Construye ρ = |ψ⟩⟨ψ| desde un estado puro.\"\"\"\n",
    "        return np.outer(state, np.conj(state))\n",
    "\n",
    "    @staticmethod\n",
    "    def mixed_state_entropy(probabilities: List[float],\n",
    "                           density_matrices: List[np.ndarray]) -> float:\n",
    "        \"\"\"Entropía para mezcla: ρ = Σᵢ pᵢ ρᵢ\"\"\"\n",
    "        rho_mixed = np.zeros_like(density_matrices[0], dtype=complex)\n",
    "        for p, rho in zip(probabilities, density_matrices):\n",
    "            rho_mixed += p * rho\n",
    "        return VonNeumannEntropy.compute_von_neumann_entropy(rho_mixed)\n",
    "\n",
