    "# ============================================================================\n",
    "# RUIDO PROBABILÍSTICO DE REFERENCIA (PRN)\n",
    "# ============================================================================\n",
    "\n",
    "class PRN:\n",
    "    \"\"\"Modelado del Ruido Probabilístico de Referencia.\"\"\"\n",
    "\n",
    "    def __init__(self, influence: float, algorithm_type: Optional[str] = None, **parameters):\n",
    "        if not 0 <= influence <= 1:\n",
    "            raise ValueError(f\"Influencia debe estar en [0,1]. Valor: {influence}\")\n",
    "        self.influence = influence\n",
    "        self.algorithm_type = algorithm_type\n",
    "        self.parameters = parameters\n",
    "\n",
    "    def adjust_influence(self, adjustment: float) -> None:\n",
    "        \"\"\"Ajusta influencia dentro de [0, 1].\"\"\"\n",
    "        new_influence = np.clip(self.influence + adjustment, 0, 1)\n",
    "        if new_influence != self.influence + adjustment:\n",
    "            print(f\"⚠️  Influencia ajustada a {new_influence}\")\n",
    "        self.influence = new_influence\n",
    "\n",
    "    def combine_with(self, other_prn: 'PRN', weight: float = 0.5) -> 'PRN':\n",
    "        \"\"\"Combina dos PRN según un peso.\"\"\"\n",
    "        if not 0 <= weight <= 1:\n",
    "            raise ValueError(f\"Peso debe estar en [0,1]. Valor: {weight}\")\n",
    "        combined_influence = self.influence * weight + other_prn.influence * (1 - weight)\n",
    "        combined_params = {**self.parameters, **other_prn.parameters}\n",
    "        algorithm = self.algorithm_type if weight >= 0.5 else other_prn.algorithm_type\n",
    "        return PRN(combined_influence, algorithm, **combined_params)\n",
    "\n",
    "    def __str__(self) -> str:\n",
    "        params_str = \", \".join(f\"{k}={v}\" for k, v in self.parameters.items())\n",
    "        algo_str = f\", algorithm={self.algorithm_type}\" if self.algorithm_type else \"\"\n",
    "        return f\"PRN(influence={self.influence:.4f}{algo_str}{', ' + params_str if params_str else ''})\"\n",
    "\n",
    "\n",
    "class EnhancedPRN(PRN):\n",
    "    \"\"\"Extiende PRN para registrar distancias de Mahalanobis.\"\"\"\n",
    "\n",
    "    def __init__(self, influence: float = 0.5, algorithm_type: str = None, **parameters):\n",
    "        super().__init__(influence, algorithm_type, **parameters)\n",
    "        self.mahalanobis_records = []\n",
    "\n",
    "    def record_quantum_noise(self, probabilities: dict, quantum_states: np.ndarray):\n",
    "        \"\"\"Registra ruido cuántico basado en Mahalanobis.\"\"\"\n",
    "        entropy = self.calculate_entropy_from_probabilities(probabilities)\n",
    "\n",
    "        cov_estimator = EmpiricalCovariance().fit(quantum_states)\n",
    "        mean_state = np.mean(quantum_states, axis=0)\n",
    "        inv_cov = np.linalg.pinv(cov_estimator.covariance_)\n",
    "\n",
    "        diff = quantum_states - mean_state\n",
    "        aux = diff @ inv_cov\n",
    "        dist_sqr = np.einsum('ij,ij->i', aux, diff)\n",
    "        distances = np.sqrt(dist_sqr)\n",
    "        mahal_mean = np.mean(distances)\n",
    "\n",
    "        self.mahalanobis_records.append(mahal_mean)\n",
    "        return entropy, mahal_mean\n",
    "\n",
    "    @staticmethod\n",
    "    def calculate_entropy_from_probabilities(probabilities: dict) -> float:\n",
    "        \"\"\"Calcula entropía de Shannon.\"\"\"\n",
    "        probs = np.array(list(probabilities.values()))\n",
    "        probs = probs[probs >= 0]\n",
    "        sum_probs = np.sum(probs)\n",
    "        if sum_probs > 0:\n",
    "            probs = probs / sum_probs\n",
    "        else:\n",
    "            probs = np.ones_like(probs) / len(probs)\n",
    "\n",
    "        probs = np.clip(probs, 1e-15, 1.0)\n",
    "        return -np.sum(probs * np.log2(probs))\n",
    "\n",
