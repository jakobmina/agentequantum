    "class QuantumNoiseCollapse(QuantumBayesMahalanobis):\n",
    "    \"\"\"Simula colapso de onda cuántico con estructura metripléctica.\"\"\"\n",
    "\n",
    "    def __init__(self, prn_influence: float = 0.5):\n",
    "        super().__init__()\n",
    "        self.prn = EnhancedPRN(influence=prn_influence)\n",
    "        self.aureo_n_step = 1\n",
    "\n",
    "    @timer_decorator\n",
    "    def simulate_wave_collapse_metriplectic(self,\n",
    "                                            quantum_states: np.ndarray,\n",
    "                                            density_matrix: np.ndarray,\n",
    "                                            prn_influence: float,\n",
    "                                            previous_action: int,\n",
    "                                            M: Optional[np.ndarray] = None):\n",
    "        \"\"\"Simula colapso de onda con estructura metripléctica.\"\"\"\n",
    "        if M is None:\n",
    "            M = np.eye(2) * 0.1\n",
    "\n",
    "        # 1. Entropía von Neumann\n",
    "        von_neumann_ent = VonNeumannEntropy.compute_von_neumann_entropy(\n",
    "            density_matrix, normalize=True, method=\"sigmoid\"\n",
    "        )\n",
    "\n",
    "        # 2. Ruido cuántico\n",
    "        magnitudes = np.array([np.linalg.norm(state) for state in quantum_states])\n",
    "        sum_magnitudes = np.sum(magnitudes)\n",
    "        if sum_magnitudes > 0:\n",
    "            normalized_magnitudes = magnitudes / sum_magnitudes\n",
    "        else:\n",
    "            normalized_magnitudes = np.ones_like(magnitudes) / len(magnitudes)\n",
    "\n",
    "        probabilities = {str(i): p for i, p in enumerate(normalized_magnitudes)}\n",
    "        entropy, mahalanobis_mean = self.prn.record_quantum_noise(probabilities, quantum_states)\n",
    "\n",
    "        # Normalizar entropía\n",
    "        entropy_normalized = 1.0 / (1.0 + np.exp(-entropy)) if entropy > 1.0 else entropy\n",
    "        entropy_normalized = np.clip(entropy_normalized, 0.0, 1.0)\n",
    "\n",
    "        mahalanobis_normalized = 1.0 / (1.0 + np.exp(-mahalanobis_mean))\n",
    "        mahalanobis_normalized = np.clip(mahalanobis_normalized, 0.0, 1.0)\n",
    "\n",
    "        # 3. Cosenos directores\n",
    "        cos_x, cos_y, cos_z = calculate_cosines(entropy_normalized, mahalanobis_normalized)\n",
    "\n",
    "        # 4. Coherencia\n",
    "        coherence = np.exp(-mahalanobis_normalized) * (cos_x + cos_y + cos_z) / 3.0\n",
    "\n",
    "        # 5. Lógica bayesiana\n",
    "        bayes_probs = self.calculate_probabilities_and_select_action(\n",
    "            entropy=entropy_normalized,\n",
    "            coherence=coherence,\n",
    "            prn_influence=prn_influence,\n",
    "            action=previous_action\n",
    "        )\n",
    "\n",
    "        # 6. Proyección cuántica\n",
    "        projected_states = self.quantum_cosine_projection(\n",
    "            quantum_states, entropy_normalized, coherence, n_step=self.aureo_n_step\n",
    "        )\n",
    "\n",
    "        # 7. Evolución metripléctica\n",
    "        q_val = np.array([entropy_normalized])\n",
    "        p_val = np.array([coherence])\n",
    "\n",
    "        metriplectic_evolution = MetriplecticStructure.metriplectic_evolution(\n",
    "            H, S, H, q_val, p_val, M\n",
    "        )\n",
    "\n",
    "        # 8. Colapso final\n",
    "        collapsed_state = tf.reduce_sum(\n",
    "            tf.multiply(\n",
    "                projected_states,\n",
    "                tf.cast(bayes_probs[\"action_to_take\"], tf.float32) * self.aureo_n_step\n",
    "            )\n",
    "        )\n",
    "\n",
    "        self.aureo_n_step += 1\n",
    "\n",
    "        return {\n",
    "            \"collapsed_state\": collapsed_state.numpy(),\n",
    "            \"action\": bayes_probs[\"action_to_take\"],\n",
    "            \"shannon_entropy\": entropy,\n",
    "            \"shannon_entropy_normalized\": entropy_normalized,\n",
    "            \"von_neumann_entropy\": von_neumann_ent,\n",
    "            \"coherence\": coherence,\n",
    "            \"mahalanobis_distance\": mahalanobis_mean,\n",
    "            \"mahalanobis_normalized\": mahalanobis_normalized,\n",
    "            \"cosines\": (cos_x, cos_y, cos_z),\n",
    "            \"metriplectic_evolution\": metriplectic_evolution,\n",
    "            \"bayesian_posterior\": bayes_probs[\"posterior_a_given_b\"]\n",
    "        }\n",
    "\n",
    "    @timer_decorator\n",
    "    def objective_function_with_noise(self,\n",
    "                                      quantum_states: tf.Tensor,\n",
    "                                      target_state: np.ndarray,\n",
    "                                      entropy_weight: float = 1.0,\n",
    "                                      n_step: int = 1) -> tf.Tensor:\n",
    "        \"\"\"Función objetivo con ruido (estabilizada).\"\"\"\n",
    "        paridad, fase_mod = aureo_operator(n_step)\n",
    "        target_modulado = tf.cast(target_state, tf.float32) * tf.cast(fase_mod, tf.float32)\n",
    "\n",
    "        fidelity = tf.abs(tf.reduce_sum(quantum_states * target_modulado))**2\n",
    "\n",
    "        quantum_states_np = quantum_states.numpy()\n",
    "        magnitudes = np.array([np.linalg.norm(state) for state in quantum_states_np])\n",
    "        sum_magnitudes = np.sum(magnitudes)\n",
    "        if sum_magnitudes > 0:\n",
    "            normalized_magnitudes = magnitudes / sum_magnitudes\n",
    "        else:\n",
    "            normalized_magnitudes = np.ones_like(magnitudes) / len(magnitudes)\n",
    "\n",
    "        probabilities_for_shannon = {str(i): p for i, p in enumerate(normalized_magnitudes)}\n",
    "        entropy, mahalanobis_dist = self.prn.record_quantum_noise(probabilities_for_shannon, quantum_states_np)\n",
    "\n",
    "        mahalanobis_dist_clipped = np.clip(mahalanobis_dist, 0, 1000.0)\n",
    "        mahalanobis_term = tf.stop_gradient(1.0 - tf.exp(-tf.cast(mahalanobis_dist_clipped, tf.float32)))\n",
    "\n",
    "        paridad_tf = tf.cast(paridad, tf.float32)\n",
    "        entropy_clipped_tf = tf.cast(np.clip(entropy, 0, 1000.0), tf.float32)\n",
    "        entropy_weight_tf = tf.cast(entropy_weight, tf.float32)\n",
    "\n",
    "        paridad_term = tf.abs(tf.reduce_mean(quantum_states) - paridad_tf)\n",
    "\n",
    "        objective_value = (\n",
    "            (1.0 - fidelity)\n",
    "            + entropy_weight_tf * entropy_clipped_tf\n",
    "            + mahalanobis_term\n",
    "            + 0.5 * paridad_term\n",
    "        )\n",
    "\n",
    "        return objective_value\n",
    "\n",
    "    @timer_decorator\n",
    "    def optimize_quantum_state(self,\n",
    "                               initial_states: np.ndarray,\n",
    "                               target_state: np.ndarray,\n",
    "                               max_iterations: int = 100,\n",
    "                               learning_rate: float = 0.01):\n",
    "        \"\"\"Optimiza estados cuánticos mediante descenso de gradiente (Adam).\"\"\"\n",
    "        current_states = tf.Variable(initial_states.astype(np.float32), dtype=tf.float32)\n",
    "\n",
    "        best_objective = float('inf')\n",
    "        best_states = current_states.numpy().copy()\n",
    "\n",
    "        optimizer = tf.keras.optimizers.Adam(learning_rate=learning_rate)\n",
    "\n",
    "        for i in range(1, max_iterations + 1):\n",
    "            with tf.GradientTape() as tape:\n",
    "                objective = self.objective_function_with_noise(current_states, target_state, n_step=i)\n",
    "\n",
    "            grads = tape.gradient(objective, [current_states])\n",
    "\n",
    "            if grads[0] is None or tf.reduce_any(tf.math.is_nan(grads[0])):\n",
    "                print(f\"⚠️  Deteniendo en iteración {i} por gradiente inválido\")\n",
    "                break\n",
    "\n",
    "            optimizer.apply_gradients(zip(grads, [current_states]))\n",
    "\n",
    "            new_objective = self.objective_function_with_noise(current_states, target_state, n_step=i)\n",
    "            if new_objective < best_objective:\n",
    "                best_objective = new_objective\n",
    "                best_states = current_states.numpy().copy()\n",
    "\n",
    "        return best_states, float(best_objective)\n",
